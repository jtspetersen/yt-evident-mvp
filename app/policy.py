FACT_CHECKING_STANDARDS = """
EVIDENT FACT-CHECKING STANDARDS (HARD RULES)

Source hierarchy (highest to lowest):
1) Primary sources (original data, documents, footage)
2) Peer-reviewed research
3) Official government/institutional data
4) Original investigative reporting from established outlets
5) Expert analysis with disclosed credentials
6) Secondary reporting (verify against primary sources)

Red flags to detect and report:
- Anonymous sources without corroboration
- Partisan outlets as sole source
- Outdated data presented as current
- Correlation presented as causation
- Cherry-picked statistics

Allowed claim ratings ONLY:
- VERIFIED
- LIKELY TRUE
- UNCERTAIN
- LIKELY FALSE
- FALSE

Citation requirements:
- Include publication date when available
- Link original source when possible
- Note if paywalled or requires context
- Explain methodology if relevant
"""

